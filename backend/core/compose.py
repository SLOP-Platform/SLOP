def build_socket_proxy_fragment(
    network_name: str = STACK_NETWORK,
    config_root: str = "",
) -> dict[str, Any]:
    """Build compose fragment for Tecnativa docker-socket-proxy.

    The proxy sits between Traefik and the Docker socket, restricting
    Traefik to read-only API calls only. This prevents Traefik from
    executing arbitrary Docker operations even if compromised.

    Traefik is configured to use this proxy instead of the raw socket.
    """
    volumes = ["/var/run/docker.sock:/var/run/docker.sock:ro"]
    if config_root:
        tmpl = f"{config_root}/socket-proxy/haproxy.cfg.template"
        volumes.append(f"{tmpl}:/usr/local/etc/haproxy/haproxy.cfg.template:ro")

    return {
        "image": "tecnativa/docker-socket-proxy:v0.4.2",  # last-verified: 2026-06-07
        "container_name": "docker-socket-proxy",
        "networks": [network_name],
        "environment": {
            # Allowlist: only what Traefik needs
            "CONTAINERS": "1",
            "SERVICES": "1",
            "NETWORKS": "1",
            "TASKS": "1",
            "EVENTS": "1",
            "INFO": "1",
            # Required for Traefik's Docker API version negotiation.
            # Without PING=1, the proxy blocks /_ping, negotiation fails, and
            # Traefik's client gets stuck at its SDK minimum (v1.24) which
            # Docker 29+ rejects (min 1.40).
            "PING": "1",
            # Traefik v3 calls /version to retrieve server info on startup.
            # Without VERSION=1 the proxy returns 403 and the provider fails.
            "VERSION": "1",
            # Deny write operations
            "VOLUMES": "0",
            "IMAGES": "0",
            "AUTH": "0",
            "POST": "0",
            "DELETE": "0",
        },
        "volumes": volumes,
        "expose": ["2375"],
        "restart": "unless-stopped",
        "security_opt": ["no-new-privileges:true"],
    }
