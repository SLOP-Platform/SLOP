"""backend/core/compose.py

Builds and merges Docker Compose fragments.

Each deployed service has its own fragment stored at:
  {data_dir}/compose/{key}.yaml

The final docker-compose.yml is produced by merging all active fragments.
This means adding or removing a service is surgical — no regenerating
the whole stack from scratch.

The Traefik fragment is special: it's generated by the platform wizard
and always included. All other fragments are generated from manifests.
"""

