_MANAGED_LABEL = "slop.managed=true"


def _apply_managed_label(service_dict: dict[str, Any]) -> dict[str, Any]:
    """Ensure a service dict carries the slop.managed=true label.

    This is the single enforcement point (ADR 0017 §D): every service written
    via write_fragment gets the label regardless of which builder produced it.
    Handles both list[str] labels (``["key=val", ...]``) and dict labels
    (``{"key": "val", ...}``); normalises to list form to match the rest of the
    codebase.  Idempotent — calling twice is safe.
    """
    existing = service_dict.get("labels")
    if existing is None:
        service_dict["labels"] = [_MANAGED_LABEL]
    elif isinstance(existing, dict):
        # Convert dict shape to list form, then insert if absent.
        label_list = [f"{k}={v}" for k, v in existing.items()]
        if _MANAGED_LABEL not in label_list:
            label_list.insert(0, _MANAGED_LABEL)
        service_dict["labels"] = label_list
    else:
        # list[str] shape — normalise in-place equivalent
        labels: list[str] = list(existing)
        if _MANAGED_LABEL not in labels:
            labels.insert(0, _MANAGED_LABEL)
        service_dict["labels"] = labels
    return service_dict


def write_fragment(
    key: str,
    service_def: dict[str, Any],
    network_name: str = STACK_NETWORK,
    named_volumes: list[str] | None = None,
) -> Path:
    """Write a service compose fragment to disk.

    Includes a top-level networks: external declaration so each fragment can
    be run standalone with docker compose -f key.yaml up -d without Compose
    complaining that the network is undefined.

    ``named_volumes`` — Docker named volumes the service references (e.g. a
    managed DB's ``<svc>_data``). They are declared in a top-level ``volumes:``
    block so compose can resolve them when the fragment runs standalone; without
    the declaration compose errors on the undefined volume (#1203).

    Every service written here is normalised via _apply_managed_label so that
    slop.managed=true is always present (ADR 0017 §D), regardless of which
    builder produced the fragment.
    """
    fragment_dir = config.compose_dir
    fragment_dir.mkdir(parents=True, exist_ok=True)

    path = fragment_dir / f"{key}.yaml"
    content: dict[str, Any] = {
        "services": {key: _apply_managed_label(service_def)},
        "networks": {network_name: {"external": True}},
    }
    if named_volumes:
        content["volumes"] = {vol: {} for vol in named_volumes}
    path.write_text(yaml.dump(content, default_flow_style=False, sort_keys=False))
    log.debug("Wrote compose fragment: %s", path)
    return path


def remove_fragment(key: str) -> None:
    """Remove a service compose fragment."""
    path = config.compose_dir / f"{key}.yaml"
    if path.exists():
        path.unlink()
        log.info("Removed compose fragment: %s", path)


def merge_fragments(network_name: str = STACK_NETWORK) -> str:
    """Merge all active fragments into a single docker-compose.yml string."""
    fragment_dir = config.compose_dir
    if not fragment_dir.exists():
        return ""

    merged: dict[str, Any] = {
        "networks": {
            network_name: {"external": True},
        },
        "services": {},
    }

    for frag_path in sorted(fragment_dir.glob("*.yaml")):
        try:
            frag = yaml.safe_load(frag_path.read_text()) or {}
            services = frag.get("services", {})
            merged["services"].update(services)
        except yaml.YAMLError as e:
            log.warning("Skipping malformed fragment %s: %s", frag_path.name, e)

    return yaml.dump(merged, default_flow_style=False, sort_keys=False)


def write_compose_file(output_path: Path, network_name: str = STACK_NETWORK) -> Path:
    """Write the merged docker-compose.yml to disk."""
    content = merge_fragments(network_name)
    output_path.write_text(content)
    log.info("Wrote docker-compose.yml: %s", output_path)
    return output_path


